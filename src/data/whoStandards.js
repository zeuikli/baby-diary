// Helper to select reference data by metric + gender
export function getWhoReference(metric, gender) {
  const isGirl = gender === 'girl'
  if (metric === 'weight') return isGirl ? WHO_WEIGHT_GIRLS : WHO_WEIGHT_BOYS
  if (metric === 'height') return isGirl ? WHO_HEIGHT_GIRLS : WHO_HEIGHT_BOYS
  if (metric === 'headCirc') return isGirl ? WHO_HEADCIRC_GIRLS : WHO_HEADCIRC_BOYS
  return []
}
